package com.skronawi.spring.examples.rest.communication;

public class RestData {

    private String id;
    private String data;

    public RestData() {
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }
}
